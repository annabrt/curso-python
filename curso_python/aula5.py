# Tipo de dado bool (boolean)
# Ao questionar algo em um programa existem só duas opções possiveis:
# sim (True) ou não (False).
# Existem ários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que 
# questiona se um valor é igual ao outro
print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Não => False (Falso)
print(type(10 == 10)) # vai retornar o tipo de dado
print(type(10 == 11)) # vai retornar o tipo de dado


