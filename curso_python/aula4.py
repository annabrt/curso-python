# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) #int

# float -> Número com ponto flotoante ou seja numeros quebrados com casas decimais
# O tipo float representa qualquer número
# pósitivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo
print(1.1, 10.11) #float

# A função type mostra o tipo que o Python inferiu ao valor. ou seja mostra o tipo
print(  type('Kiki')  ) #como exemplo vai mostrar que essa classe é uma string
print(  type(777)  ) #como exemplo vai mostrar que essa classe é do tipo inteiro